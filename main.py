@bot.message_handler(commands=['start'])
def send_welcome(message):
    markup = types.ReplyKeyboardMarkup(resize_keyboard=True)
    markup.row("Диеты", "Для чего этот бот?", "Случайный рецепт")
    bot.send_message(
        message.chat.id,
        "Привет! Это бот-диетолог, который поможет выбрать диету по вкусам и целям",
        reply_markup=markup
    )

@bot.message_handler(func=lambda m: True)
def handle_message(message):
    if message.text == "Диеты":
        markup = types.ReplyKeyboardMarkup(resize_keyboard=True)
        markup.row("Японская кухня", "Русская кухня")
        markup.row("Итальянская кухня", "Испанская кухня")
        markup.row("Индийская кухня", "Назад")
        bot.send_message(message.chat.id, "Выберите тип кухни:", reply_markup=markup)

    elif message.text == "Назад":
        markup = types.ReplyKeyboardMarkup(resize_keyboard=True)
        markup.row("Диеты", "Для чего этот бот?", "Случайный рецепт")
        bot.send_message(message.chat.id, "Главное меню", reply_markup=markup)

    elif message.text == "Для чего этот бот?":
        bot.send_message(message.chat.id, about_text)

    elif message.text == "Случайный рецепт":
        recipe = random.choice(easy_recipes)
        bot.send_message(message.chat.id, f"Вот случайный легкий рецепт:\n\n{recipe}")

    elif message.text == "Топ легких рецептов":
        recipes_text = "\n\n".join(easy_recipes)
        bot.send_message(message.chat.id, f"Вот пять простых рецептов:\n\n{recipes_text}")

    elif message.text in ["Японская кухня", "Русская кухня", "Итальянская кухня", "Испанская кухня", "Индийская кухня"]:
        options = diet_options.get(message.text, {})
        if options:
            reply_lines = [f"{num}. {name[0]}" for num, name in options.items()]
            reply_text = "Выберите номер диеты:\n" + "\n".join(reply_lines)
            bot.send_message(message.chat.id, reply_text)
            # Запоминаем, какую кухню выбрал пользователь
            # чтобы знать, откуда брать рецепт при вводе номера
            bot.register_next_step_handler(message, handle_diet_number, options)
    elif message.text.isdigit():
        # Поиск рецепта по номеру
        num = int(message.text)
        found = False
        for options in diet_options.values():
            if num in options:
                recipe = options[num][1]
                bot.send_message(message.chat.id, recipe)
                found = True
                break
        if not found:
            bot.send_message(message.chat.id, "Неверный номер. Попробуйте снова.")
    else:
        bot.send_message(message.chat.id, "Пожалуйста, выберите диету из списка или используйте меню.")

def handle_diet_number(message, options):
    if message.text.isdigit():
        num = int(message.text)
        if num in options:
            recipe = options[num][1]
            bot.send_message(message.chat.id, recipe)
        else:
            bot.send_message(message.chat.id, "Неверный номер. Попробуйте снова.")
    else:
        bot.send_message(message.chat.id, "Пожалуйста, введите номер диеты числом.")

bot.polling()